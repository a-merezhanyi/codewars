// 1 Plain solution
double getVolumeOfCuboid(final double length, final double width, final double height) {
  return length * width * height;
}

// 2 Optimized solution
double getVolumeOfCuboid(final double l, final double w, final double h) {
  return l * w * h;
}

// 3 Clever solution
double getVolumeOfCuboid(final l, final w, final h) => l * w * h;
